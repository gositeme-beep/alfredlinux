<h2>Hidden Costs to Watch For</h2>

<h3>Setup Fees</h3>
<p>Some providers charge $200-$500 for initial configuration, knowledge base setup, and training. Alfred has no setup fee — you configure your agent yourself in the dashboard.</p>

<h3>Overage Charges</h3>
<p>Exceeding your plan's included minutes or calls triggers overage rates, which are often 2-3× the per-unit rate. Check overage pricing before committing.</p>

<h3>Integration Fees</h3>
<p>Connecting to your CRM, calendar, or helpdesk may require a higher-tier plan. With some providers, each integration adds $10-25/month. Alfred includes API access and webhooks on all plans.</p>

<h3>Contract Lock-In</h3>
<p>Several virtual receptionist services require 6-12 month contracts with early termination fees. AI-first platforms like Alfred are typically month-to-month.</p>

<h3>Phone Number Porting</h3>
<p>Porting your existing business number to an AI platform can cost $10-25 as a one-time fee. Some providers include this free.</p>

