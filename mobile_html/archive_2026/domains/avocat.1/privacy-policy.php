<h2>32. Encryption Sovereignty</h2>

<h3>32.1 Encryption Architecture</h3>
<p>GoSiteMe employs a 10-layer hybrid encryption architecture combining classical and post-quantum cryptographic primitives. Your communications, files, AI interactions, robot telemetry, and device data are protected by:</p>
<ul>
    <li><strong>Kyber-768 (ML-KEM)</strong> &mdash; NIST-approved post-quantum key exchange;</li>
    <li><strong>ECDH P-256</strong> &mdash; Classical elliptic-curve key exchange;</li>
    <li><strong>AES-256-GCM</strong> &mdash; 256-bit authenticated encryption;</li>
    <li><strong>Dilithium-inspired lattice signatures</strong> &mdash; Post-quantum message authentication;</li>
    <li><strong>Double Ratchet</strong> &mdash; Per-message forward secrecy;</li>
    <li><strong>Hash Chain Integrity</strong> &mdash; Tamper-evident message chains;</li>
    <li><strong>Key Commitment</strong> &mdash; Protection against ciphertext manipulation;</li>
    <li><strong>Steganographic Obfuscation</strong> &mdash; Traffic analysis resistance.</li>
</ul>
<p>This encryption stack is designed to resist attacks from current classical computers, future quantum computers, and nation-state adversaries.</p>

<h3>32.2 Zero-Knowledge Architecture</h3>
<p>GoSiteMe operates on a zero-knowledge principle for encrypted content. We <strong>cannot</strong> read your messages, decrypt your files, listen to your calls, or access your encrypted AI conversations. Your encryption keys are generated and stored exclusively on your device. We never have access to your keys and never will.</p>

<h3>32.3 Forward Secrecy</h3>
<p>Every message is encrypted with a unique per-message key derived from a continuously ratcheting cryptographic chain. Even if an attacker compromises a single message key, they cannot decrypt any past or future messages. This protection operates automatically and requires no User action.</p>

