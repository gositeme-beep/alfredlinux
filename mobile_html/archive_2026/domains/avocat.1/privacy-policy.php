<h2>5. Data Storage & Security</h2>

<h3>5.1 Data Location</h3>
<ul>
    <li><strong>Primary Storage:</strong> Your data is stored on secure servers located in <strong>Quebec, Canada</strong>.</li>
    <li><strong>Edge Processing:</strong> Some data may be temporarily processed at edge locations for latency-sensitive operations (VR rendering, robotic device control, voice processing).</li>
    <li><strong>On-Device Processing:</strong> Certain robotics and IoT functions process data locally on the device. Locally processed data is not transmitted to our servers unless cloud features are enabled.</li>
