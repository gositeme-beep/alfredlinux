<h3>5.2 Security Measures</h3>
<ul>
    <li><strong>Encryption in Transit:</strong> All data transmitted to and from our servers is encrypted using TLS 1.3.</li>
    <li><strong>Encryption at Rest:</strong> Data at rest is protected using AES-256 encryption.</li>
    <li><strong>End-to-End Encryption:</strong> Veil Protocol messages use end-to-end encryption with post-quantum cryptographic algorithms. We cannot access encrypted message content.</li>
    <li><strong>Access Controls:</strong> Role-based access control (RBAC), multi-factor authentication for administrative access, and principle of least privilege.</li>
    <li><strong>Network Security:</strong> Firewalls, intrusion detection/prevention systems, DDoS mitigation, and continuous security monitoring.</li>
    <li><strong>Workspace Isolation:</strong> User hosting environments and IDE workspaces are isolated in containers with separate encryption keys.</li>
    <li><strong>Regular Audits:</strong> Periodic security assessments, penetration testing, and vulnerability scanning.</li>
    <li><strong>Employee Training:</strong> All staff undergo regular data protection and security awareness training.</li>
</ul>
<p>While we implement industry-leading security measures, <strong>no system is 100% secure</strong>. You are responsible for securing your account credentials, device access, and maintaining your own backups.</p>

<h3>5.3 Biometric Data Security</h3>
<p>Biometric data (voice prints, VR behavioral biometrics) is stored separately from other personal data with additional encryption layers and access restrictions. Biometric data is never shared with third parties except as required by law.</p>

