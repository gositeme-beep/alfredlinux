    Robotic Devices and IoT Devices in your home may collect ambient data including audio, video, temperature, movement, and environmental information. You are responsible for informing all household members, guests, and visitors about the presence and data collection capabilities of these devices.
</div>

<h3>10.1 What Robots Collect</h3>
<ul>
    <li><strong>Navigation Data:</strong> Spatial mapping, SLAM-generated floor plans, obstacle locations, and movement paths. This data is primarily processed on-device.</li>
    <li><strong>Camera Data:</strong> Visual data for navigation, object recognition, and user interaction. Raw video is processed on-device and is <strong>NOT</strong> continuously uploaded to our servers unless you explicitly enable cloud recording.</li>
    <li><strong>Audio Data:</strong> Microphone input for wake-word detection and voice commands. Audio is processed locally for wake-word detection; only post-wake-word audio is transmitted to our servers for voice command processing.</li>
    <li><strong>Interaction Data:</strong> Touch inputs, gesture commands, spoken commands, and behavioral patterns of interaction with the device.</li>
    <li><strong>Environmental Data:</strong> Ambient sensor readings (temperature, humidity, air quality, light levels) that may characterize your home environment.</li>
</ul>

<h3>10.2 Your Controls</h3>
<ul>
    <li>You can disable camera, microphone, and individual sensors through device settings or physical hardware switches (where available).</li>
    <li>You can view and delete collected data through your account dashboard.</li>
    <li>You can disable cloud data synchronization and keep all data on-device only.</li>
    <li>You can request a complete data export of all device-collected data.</li>
    <li>You can factory reset devices to erase all on-device data.</li>
</ul>

<h3>10.3 Third-Party IoT Integrations</h3>
<p>If you connect third-party smart home devices or platforms to our Services, data exchange between systems is governed by both our Privacy Policy and the third party's privacy policy. We are not responsible for the data practices of third-party IoT devices or platforms.</p>

<h3>10.4 GoSiteMe-Manufactured Hardware (Alfred Robot) Data</h3>
<p>Alfred Robots manufactured by GoSiteMe collect additional data specific to their operation:</p>
<ul>
    <li><strong>Spatial Mapping:</strong> LIDAR and camera-based floor plans, obstacle maps, and navigation routes are stored <strong>on-device</strong> by default. Cloud synchronization is available but optional.</li>
    <li><strong>Diagnostic Telemetry:</strong> Motor performance, battery health, sensor calibration, temperature readings, and error logs are transmitted to GoSiteMe servers for product safety monitoring, quality improvement, and predictive maintenance. This data is not personally identifiable.</li>
    <li><strong>Crash &amp; Safety Event Data:</strong> In the event of a collision, fall, emergency stop, or safety anomaly, the Robot automatically records and transmits a safety event report (including sensor data, motor states, and environmental readings from the 30 seconds preceding the event) for safety investigation.</li>
    <li><strong>Firmware Update Data:</strong> Current firmware version, update history, and update status are transmitted to manage OTA updates.</li>
</ul>
<p><strong>Data on Device Return/Disposal:</strong> When GoSiteMe Hardware is returned under warranty, traded in, or sent for recycling, GoSiteMe performs certified data destruction (secure overwrite + cryptographic erasure) on all personal data stored on the device. A data destruction certificate is available upon request.</p>

